    private void usuarioRegistro(String Ddni, String Dnombre, String Dtelefono, String Dusuario, String Dcontraseña) {
        
        
        Connection dbconn = ConeccionAlLogin.connectDB();
        if(dbconn != null){
        
        try {
            PreparedStatement st= (PreparedStatement)
                    dbconn.prepareStatement("INSERT INTO datos (dni,nombre,"
                            + "telefono,usuario,contraseña) VALUES(?,?,?,?,?)");
            
           
            st.setString(1,Ddni);
            st.setString(2,Dnombre);
            st.setString(3,Dtelefono);
            st.setString(4,Dusuario);
            st.setString(5,Dcontraseña);
            
            
            int res=st.executeUpdate();       
            JOptionPane.showMessageDialog(this, "Datos ingresados en la base "
                    + "de datos","ÉXITO",JOptionPane.INFORMATION_MESSAGE);
            
            txtdni.setText("");
            txtNombre.setText("");
            txtTelefono.setText("");
            txtUsuario.setText("");           
            txtcontraseña.setText("");
            
        } catch (SQLException ex) {
            Logger.getLogger(JFrame_MenuInicio.class.getName()).log(Level.SEVERE, null, ex);
        }
        }else{
            System.out.println("la conexión no es valida ");
        }
    }
