    private void BtnCrearUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCrearUsuarioActionPerformed
        
         // TODO add your handling code here:
        String Ddni=txtdni.getText();
        String Dnombre=txtNombre.getText();
        String Dtelefono=txtTelefono.getText();        
        String Dusuario=txtUsuario.getText();
        String Dcontraseña=txtcontraseña.getText();
        
        
        if(Ddni.isEmpty() || Dnombre.isEmpty() || Dtelefono.isEmpty() 
                || Dusuario.isEmpty() || Dcontraseña.isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Rellenar los espacios",
                    "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!PASSWORD_PATTERN.matcher(Dcontraseña).matches()) {
            JOptionPane.showMessageDialog(this, " La contraseña debe cumplir "
                    + "con los siguientes requerimientos:\n"
                    + "- Entre 4 y 8 caracteres\n"
                    + "- Al menos una letra mayúscula\n"
                    + "- Al menos una letra minúscula\n"
                    + "- Al menos un número", 
                    
                    "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        usuarioRegistro(Ddni,Dnombre,Dtelefono,Dusuario,Dcontraseña); 
    }//GEN-LAST:event_BtnCrearUsuarioActionPerformed

